# atribuição simples
saldo = 1000
print("Saldo inicial:", saldo)

# sobrescrevendo a variável saldo
saldo = 100
print("Saldo após sobrescrever:", saldo)

# atribuição com adição
saldo += 500
print("Saldo após depósito de 500:", saldo)

# atribuição com subtração
saldo -= 200
print("Saldo após saque de 200:", saldo)

# atribuição com multiplicação
saldo *= 10
print("Saldo após multiplicação por 10:", saldo)

# atribuição com divisão
saldo /= 4
print("Saldo após divisão por 4:", saldo)

# atribuição com divisão inteira
saldo //= 3
print("Saldo após divisão inteira por 3:", saldo)

# atribuição com módulo
saldo %= 7
print("Saldo após módulo por 7:", saldo)

# atribuição com exponenciação
saldo **= 2
print("Saldo após exponenciação ao quadrado:", saldo) 