# Convertendo tipo float para int
print(int(10.5))  # Saída: 10

# Passando numero inteiro para string
print(int("10"))  # Saída: 10

# convertendo float para float
print(float(10.10))  # Saída: 10.1

# Convertendo int para float
print(float(10))  # Saída: 10.0

# convertendo float para string
# O type serve para identificar o tipo do dado
print(type(str(10.5))) # Saída: '10.5'

