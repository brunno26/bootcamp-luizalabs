# Os dados são acessados e modificados através das chaves.add()

# Criando um dicionário
dados = {"nome": "Guilherme", "idade": 28, "telefone": "33731649"}

# Acessando dados / Não tem atribuição de valor.
print(dados["nome"])
print(dados["idade"])
print(dados["telefone"])

# Inserindo ou modoficando dados/ Tem atribuição de valor.
dados["nome"] = "Mariana"
dados["idade"] = 33
dados["telefone"] = "984079773"

print(dados)