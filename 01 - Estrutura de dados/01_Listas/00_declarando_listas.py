# Listas em python podem armazenar de maneira sequêncial qualquer tipo de objeto. Podemos criar listas utilizando o constrututor "List", a função range, ou colocando valores separados por vírgula dentro de colchetes. As listas são objetos mutáveis, portanto podemos alterar seus valores após a criação.

frutas = ["Laranja", "Maçã", "Banana"]
print(frutas)

frutas = []
print(frutas)

letras = list("python")
print(letras)

numeros = list(range(10))
print(numeros)

carro = ["Lamborgini", "F8", 500000, 2025, "São Paulo", True]
print(carro)