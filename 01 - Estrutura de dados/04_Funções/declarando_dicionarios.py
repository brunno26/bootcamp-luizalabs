# Criando um dicionário
pessoa = {"nome": "Guilherme", "idade": 28}
print(pessoa)

# O dict é uma função usada para adicionar argumentos nomeados chave=valor
pessoa = dict(nome="Guilherme", idade=28)
print(pessoa)

# Adicionando um dado no formato chave-valor
pessoa["telefone"] = "3373-1649"
print(pessoa)


